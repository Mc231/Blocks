    case l90Tetramonio = 3
    case j90Tetramonio = 4
    case l180Tetramonio = 5
    case jTetramonio = 6
    case j270Tetramonio = 7
    case l270Tetramonio  = 8
    case j180Tetramonio = 9
    case lTetramonio = 10
    case sTetramonio = 11
    case s90Tetramonio  = 12
    case zTetramonio = 13
    case z90Tetramonio = 14
    case t180Tetramonio = 15
    case tTetramonio = 16
    case t90Tetramonio = 17
    case t270Tetramonio = 18
    case noneTetramonio = -1
}
